export function getIdolImages(params) {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      const {name} = params;

      function getRandomValues(arr, count) {
        const values = [];
        for (let i = 0; i < count; i++) {
          const randomIndex = Math.floor(Math.random() * arr.length);
          values.push(arr.splice(randomIndex, 1)[0]);
        }
        return values;
      }

      let cx = [
        '219349120_800300317313609_7691747447293727933_n.jpeg',
        '299180709_475571564022110_5374496400830654462_n.jpeg',
        '300303054_1084974882129458_4224607917202559964_n.jpeg',
        '306888316_162876029730162_4854338851575757367_n.jpeg',
        '311041305_892539975485437_3147370924777353167_n.jpeg',
        '312307266_181255794448433_525019733613779645_n.jpeg',
        '313473116_1801367296900889_5407976365198015837_n.jpeg',
        '314516581_1129578357676896_6431834982245957333_n.jpeg',
        '315761796_454019013338901_3773517364246318115_n.jpeg',
        '317639905_446871580960868_6108901046441575627_n.jpeg',
        '319801814_154315077378726_9055366276893967024_n.jpeg',
      ]

      let oynn = [
        '229323942_834978387132102_4129135032380312924_n.jpeg',
        '260275992_443951064017388_8120694513404601001_n.jpeg',
        '279928391_964488074221469_6104304098134337017_n.jpeg',
        '310812091_1108728969758021_1027002061431010457_n.jpeg',
        '314929026_818925446058506_7024881548225654245_n.jpeg',
        '315894396_187630870504862_4616335074465432266_n.jpeg',
        '316029231_111158778379978_7320176015818593328_n.jpeg',
        '321284368_709465150535803_6365083911674404453_n.jpeg',
        '321706556_919666395885228_1046310628334209128_n.jpeg',
        '321821533_745215853602141_8389795013703335073_n.jpeg',
        '321992406_1225826614636485_8097102520988187675_n.jpeg',
      ]

      let data = null;
      if (name === '程潇') {
        data = cx;
      } else if (name === '欧阳娜娜') {
        data = oynn;
      } else {
        data = getRandomValues([...cx, ...oynn], 5)
      }

      data = data.map(item => `./images/${item}`)

      resolve(data)
    }, Math.random() * 500)
  })
}